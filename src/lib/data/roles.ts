function generateCharCode() {
    return [
        ...Array.from({ length: 20 }).map((_, i) => {
            const char = String.fromCharCode(97 + i);
            const indicator = String.fromCodePoint(0x1f1e6 + i);
            const encoded = encodeURIComponent(indicator);
            return {
                emoji: `:regional_indicator_${char}:`,
                encoded,
            };
        }),
    ];
}

